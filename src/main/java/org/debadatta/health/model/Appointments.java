package org.debadatta.health.model;

import org.springframework.context.annotation.Scope;
import org.springframework.stereotype.Component;
import java.util.Date;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBDocument;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;

import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;

@Data
@NoArgsConstructor
@AllArgsConstructor
@Component
@Scope("prototype")
@DynamoDBDocument
@DynamoDBTable(tableName = "Appointments")
public class Appointments {

    @DynamoDBAutoGeneratedKey
    @DynamoDBHashKey(attributeName = "a_id")
    private int a_id;
    @DynamoDBAttribute(attributeName = "p_id")
    private String p_id;
    @DynamoDBAttribute(attributeName = "d_id")
    private String d_id;
    @DynamoDBAttribute(attributeName = "a_date")
    private Date a_date;
    @DynamoDBAttribute(attributeName = "a_time")
    private String a_time;
    @DynamoDBAttribute(attributeName = "status")
    private String status;

}
